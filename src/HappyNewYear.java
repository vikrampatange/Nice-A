import java.util.*;

public class HappyNewYear {

	
	public static void main(String[] args) {
	
		int year =Calendar.getInstance().get(Calendar.YEAR);
		int newYear=2015;
		
		if(year==newYear)
		{
			System.out.println("Happy New Year.........!");
		}
		else{
			
			System.out.println("Good By 2014.........!!");
		}

	}

}
